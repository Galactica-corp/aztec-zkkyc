import { verifyWebhookDigest } from "../../src/sumsub/webhookDigest.js";

describe("verifyWebhookDigest", () => {
    const secret = new TextEncoder().encode("SoMe_SeCrEt_KeY");
    const data = new TextEncoder().encode("someText");

    it("HMAC_SHA256_HEX: returns true for valid digest", () => {
        const expected = new Uint8Array([
            0x7f, 0x3b, 0xee, 0xdc, 0xe7, 0x52, 0x96, 0x16, 0xa9, 0xd6, 0xbb, 0xa5, 0x9d, 0xc1, 0xb0, 0x54,
            0xa7, 0x24, 0x7c, 0xef, 0x67, 0xf6, 0xac, 0xff, 0x26, 0x00, 0x86, 0x43, 0x99, 0xf4, 0x7f, 0xe0,
        ]);
        expect(verifyWebhookDigest(secret, expected, "HMAC_SHA256_HEX", data)).toBe(true);
    });

    it("HMAC_SHA512_HEX: returns true for valid digest", () => {
        const expected = new Uint8Array([
            0x3a, 0xae, 0x08, 0x98, 0x60, 0x27, 0x05, 0x1c, 0xa0, 0x65, 0x6d, 0x87, 0xc4, 0xc9, 0x5f, 0x1f,
            0x15, 0xd3, 0xec, 0xaf, 0x23, 0xcf, 0x23, 0x9e, 0x5a, 0x43, 0xfd, 0x03, 0xa8, 0xaf, 0x28, 0x22,
            0x3a, 0x06, 0xbe, 0x1f, 0x97, 0x62, 0x4c, 0xae, 0xfd, 0x55, 0x47, 0x5c, 0x84, 0x67, 0x37, 0x73,
            0xdc, 0x10, 0xe8, 0x95, 0xa1, 0xab, 0x88, 0x5a, 0x35, 0x48, 0xc5, 0x8b, 0xd7, 0xaf, 0x9e, 0xbf,
        ]);
        expect(verifyWebhookDigest(secret, expected, "HMAC_SHA512_HEX", data)).toBe(true);
    });

    it("returns false for wrong digest", () => {
        const wrong = new Uint8Array(32);
        wrong[0] = 0xff;
        expect(verifyWebhookDigest(secret, wrong, "HMAC_SHA256_HEX", data)).toBe(false);
    });

    it("returns false for unsupported algorithm", () => {
        const expected = new Uint8Array(32);
        expect(verifyWebhookDigest(secret, expected, "HMAC_SHA384_HEX", data)).toBe(false);
    });
});
